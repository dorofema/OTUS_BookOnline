CREATE TABLE `Books` (
	`BookID` INTEGER NOT NULL AUTO_INCREMENT UNIQUE,
	`BookName` VARCHAR(255) NOT NULL,
	`CategoryID` INTEGER NOT NULL,
	PRIMARY KEY(`BookID`)
);


