	`PriceID` INTEGER NOT NULL,
	`DiscountPercent` DECIMAL NOT NULL,
	PRIMARY KEY(`OrderRow`)
);


CREATE TABLE `Statuses` (
	`StatusID` INTEGER NOT NULL AUTO_INCREMENT UNIQUE,
