	PRIMARY KEY(`StatusID`)
);


CREATE TABLE `Promocodes` (
	`PromocodeID` INTEGER NOT NULL AUTO_INCREMENT UNIQUE,
	`Promocode` VARCHAR(255) NOT NULL,
	`DiscountPercent` DECIMAL NOT NULL,
	PRIMARY KEY(`PromocodeID`)
